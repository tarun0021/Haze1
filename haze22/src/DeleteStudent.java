    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String Name=jTextField10.getText();
        String University_Roll_No=jTextField11.getText();
        try
        {
            Connection con=ConnectionProvider.getcon();
            PreparedStatement ps=con.prepareStatement("delete from student where Name=? and University_Roll_No=?");
            ps.setString(1, Name);
            ps.setString(2, University_Roll_No);
            ps.executeUpdate();
            JFrame jf=new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf,"Successfully Deleted");
            setVisible(false);
            new DeleteQuestion().setVisible(true);
            
            
            
        }
        catch(Exception e)
        {
          JFrame jf=new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf, e);  
        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

