    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         String Name=jTextField10.getText();
         String University_Roll_No=jTextField11.getText();
        try
        {
            Connection con=ConnectionProvider.getcon();
            Statement st=con.createStatement
            (ResultSet.TYPE_SCROLL_INSENSITIVE,
                ResultSet.CONCUR_UPDATABLE);
            ResultSet rs=st.executeQuery("select *from student where Name='"+Name+"' and University_Roll_No='"+University_Roll_No+"'");
            if(rs.first())
            {
                jTextField1.setText(rs.getString(1));
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jTextField6.setText(rs.getString(6));
                jTextField7.setText(rs.getString(7));
                jTextField8.setText(rs.getString(8));
                jTextField9.setText(rs.getString(9));
                jTextField10.setEditable(false);
                jTextField11.setEditable(false);

            }
        }
        catch(Exception e)
        {
            JFrame jf=new JFrame();
            jf.setAlwaysOnTop(true);
            JOptionPane.showMessageDialog(jf,"Record Does Not Exist");

        }
    }//GEN-LAST:event_jButton4ActionPerformed

